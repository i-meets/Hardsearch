package hard.engenharia.model;

public class Maquina {

    private int cod_maq;
    private String codMaquina;
    private String descricao_maq;

    public int getCod_maq() {
        return cod_maq;
    }

    public void setCod_maq(int cod_maq) {
        this.cod_maq = cod_maq;
    }

    public String getCodMaquina() {
        return codMaquina;
    }

    public void setCodMaquina(String codMaquina) {
        this.codMaquina = codMaquina;
    }

    public String getDescricao_maq() {
        return descricao_maq;
    }

    public void setDescricao_maq(String descricao_maq) {
        this.descricao_maq = descricao_maq;
    }

}
